#Ingreso de Datos
n = int(input("Ingrese numero: "))
nombre = input("Ingresa un nombre: ")

# While
i = 0               # iniciando contador,
                                        # OPCIONAL
                                        # Explicar por que suele usarse el 0 como valor de inicio
                                        # Python como C/C++ entre otros son lenguajes con colecciones basadas en
                                        # Zero-based index

while i < n:      # evaluando condicion
    print(nombre)
    i += 1          # incrementando contador

